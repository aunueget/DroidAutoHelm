package com.example.bluedrive;

public class Point2D {
	public double x;

	public double y;
	
	public Point2D(double myX, double myY){
		x=myX;
		y=myY;
	}
	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}


}
